package com.example.Password_manager.category;

public class imageType {

    private int imagesrc;
    private String name;

    public imageType(int imagesrc, String name) {
        this.imagesrc = imagesrc;
        this.name = name;
    }

    public int getImagesrc() {
        return imagesrc;
    }

    public String getName() {
        return name;
    }
}
